        //
        $request->validate(
            [
                "name"=>"required",
                "email"=>"required",
                "phone"=>"required",
                "password"=>"required"
            ],
            [
                "name.requried"=>"Enter Name",
                "email.requried"=>"Enter Email",
                "phone.requried"=>"Enter Phone",
                "password.requried"=>"Enter password",
               
            
            ]
            );
            User::create($request->except('_token'));
            return redirect()->route('users.index')->with('success',' Registration is successufll');
    }

